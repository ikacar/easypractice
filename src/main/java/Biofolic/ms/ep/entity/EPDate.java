package Biofolic.ms.ep.entity;

public class EPDate {
    String date;
    boolean available;

    public EPDate() {
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public boolean isAvailable() {
        return available;
    }

    public void setAvailable(boolean available) {
        this.available = available;
    }
}
