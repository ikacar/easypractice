package Biofolic.ms.ep.entity;

public class DateBooked {
    String start;
    String end;

    public DateBooked() {
    }

    public String getStart() {
        return start;
    }

    public void setStart(String start) {
        this.start = start;
    }

    public String getEnd() {
        return end;
    }

    public void setEnd(String end) {
        this.end = end;
    }
}
